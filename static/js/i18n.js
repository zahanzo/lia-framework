// Ensure _currentLang is global so other modules can update it
window._currentLang = "en"; 

// Global translation function
function _(key) {
  // Always use the global language variable
  return (_uiStrings[window._currentLang] || _uiStrings["en"])[key] || (_uiStrings["en"][key] || key);
}

function applyLangToUI() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = _(key);
    if (val && val !== key) el.textContent = val;
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    const val = _(key);
    if (val && val !== key) el.placeholder = val;
  });

  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    const val = _(key);
    if (val && val !== key) el.title = val;
  });
  document.documentElement.lang = window._currentLang === 'pt' ? 'pt-BR' : 'en';

  // Dispatch an event so other parts of the system (like main.js) know the UI updated
  document.dispatchEvent(new Event('i18n-updated'));
}

// Global functions for the system
window._ = _;
window.applyLangToUI = applyLangToUI;

// Initialize UI
applyLangToUI();